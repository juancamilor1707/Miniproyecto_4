package com.example.miniproyecto4.model.Validation;

/**
 * Enumeration representing the possible orientations for ship placement
 * in the Battleship game.
 */
public enum Orientation {
    /**
     * Horizontal orientation where the ship extends along the x-axis.
     */
    HORIZONTAL,

    /**
     * Vertical orientation where the ship extends along the y-axis.
     */
    VERTICAL
}